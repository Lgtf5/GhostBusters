public class ghostModel {
    
    private int id;
    private String name;
    private String class;
    private int level;
    private String date;
    private String hability;
    private String afinity;

public ghostModel(int id, String name, String class, int level, String date, String hability, String afinity){

    this.id = id;
    this.name = name;
    this.class = class;
    this.level = level;
    this.date = date;
    this.hability = hability;
    this.afinity = afinity;
}

}
